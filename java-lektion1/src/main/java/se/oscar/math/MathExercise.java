package se.oscar.math;

public class MathExercise {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 2;

        // Addition, Subtraction, Multiplication and Division //
        int sumA = num1 + num2;
        int sumB = num1 - num2;
        int sumC = num1 * num2;
        int sumD = num1 / num2;
        System.out.print(sumA + "\n" + sumB + "\n" + sumC + "\n" + sumD);
    }
}
